export const translateMealLine = (text = "", language = "en") => {
  const value = String(text || "");
  if (!value || language === "en") return value;
  const replacements = [
    ...(CLEAN_PHRASE_REPLACEMENTS[language] || []),
    ...(PHRASE_REPLACEMENTS[language] || []),
  ];
  const phraseTranslated = replacements.reduce(
    (acc, [from, to]) => acc.split(from).join(to),
    value
  );
  const wordReplacements =
    CLEAN_WORD_REPLACEMENTS[language] || WORD_REPLACEMENTS[language] || [];
  return applyWordReplacements(phraseTranslated, wordReplacements);
};

